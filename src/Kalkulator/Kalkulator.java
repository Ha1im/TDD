package Kalkulator;

public class Kalkulator {
	public int sabiranje(int number1, int number2) {
		int result = number1 + number2;
		return result;
	}

	public int oduzimanje(int number1, int number2) {
		int result = number1 - number2;
		return result;
	}

	public int mnozenje(int number1, int number2) {
		int result = number1 * number2;
		return result;
	}

	public int dijeljenje(int number1, int number2) {
		int result = number1 / number2;
		return result;
	}

}